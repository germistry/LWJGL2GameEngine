package textures;

//Simple class that represents the texture of the terrain model. 
public class TerrainTexture {

	private int textureID;

	//Simple constructor for the textureID property.
	public TerrainTexture(int textureID) {
		this.textureID = textureID;
	}
	//Need only a getter.
	public int getTextureID() {
		return textureID;
	}
	
}
